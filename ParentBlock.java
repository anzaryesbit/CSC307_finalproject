public class ParentBlock {
    private int x;
    private int y;
    private String type;

    public ParentBlock(int x, int y, String type) {
        this.x = x;
        this.y = y;
        this.type = type;
    } 

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public String getType() {
        return this.type;
    }
}
